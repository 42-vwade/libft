#include <string.h>

void	*ft_memset(void *ptr, int x, size_t n)
{
	int		i;
	char	*dup;

	i = 0;
	while (n-- > 0)
	{
		dup = &ptr[i];
		dup[0] = (unsigned char)x;
		i++;
	}
	return (ptr);
}
