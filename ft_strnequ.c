#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	if (!(i = 0) && (!s1 || !s2))
		return (0);
	while (s1[i] == s2[i] && (n-- > 0) && (s1[i] && s2[i]))
		i++;
	return ((s1[i] == s2[i]) ? 1 : 0);
}
