#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*p;

	p = s1;
	s1 += ft_strlen(s1);
	while ((s1[0] = s2[0]) && n--)
		if (!*++s1 || !*++s2)
			break ;
	s1[0] = 0;
	return (p);
}
