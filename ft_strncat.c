#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	s;

	i = 0;
	s = ft_strlen(s1);
	while (i < n && (s2[i]))
	{
		s1[s] = s2[i];
		s++;
		i++;
	}
	s1[s] = 0;
	return (s1);
}
