int		ft_atoi(char *str)
{
	int num;

	num = 0;
	while (*str > 0 && *str <= 32)
		str++;
	if (*(str + 1) >= '0' && *(str + 1) <= '9')
		if (*str == '-' || *str == '+')
			return ((*str == '+') ? ft_atoi(++str) : -ft_atoi(++str));
	while (*str && (*str >= '0' && *str <= '9'))
		num = num * 10 + (*str++ - 48);
	return (num);
}
