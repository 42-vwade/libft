int		ft_isalpha(int c)
{
	unsigned char a;

	if ((unsigned)c > 0x100)
		return (0);
	a = (unsigned char)c;
	if ((a >= 'A' && a <= 'Z') || (a >= 'a' && a <= 'z'))
		return (1);
	else
		return (0);
}
